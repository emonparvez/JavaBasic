package JavaBasic_Codes;

public class MultidimensionalArray_For_Each {

public static void main(String[] args) {
   
    
    int MultiArray [][]={{1,2,3},{4,5,6},{7,8,11}};
    
  
    
    
    for(int i[] : MultiArray){
        for(int j :i){
   
        System.out.print(j +" ");

        }
        
        System.out.println();


    }
    
    
    
    
    
    
}




    
}
